package _03_loops;

public class _4_odd_Even_num {

	public static void main(String[] args) {
		
		
	    for (int i = 1; i <= 10; i++) {
	         if (i % 2 == 0) {
	            System.out.println(i + " is even");
	         }
	      }

	      
	      
	    for (int i = 1; i <= 10; i++) {
	         if (i % 2 != 0) {
	            System.out.println(i + " is odd");
	         }
	      }

	}

}
