package _05_static;

public class _01_methods_and_variables {

	
	// Static variables
	    public static int Var1 = 0;
	    public static String Var2 = "Hello";
	    
	// Instance variables
	    private int instanceVar1;
	    private String instanceVar2;
	    
	 // Static methods
	    public static int StaticVar1() {
	        return staticVar1;
	    }

	    public static String StaticVar2() {
	        return staticVar2;
	    }
	    
	 // Instance methods
	    public int getInstanceVar1() {
	        return instanceVar1;
	    }

	    public String getInstanceVar2() {
	        return instanceVar2;
	    }
	// Main Method
	    public static void main(String[] args) {
	        
	        System.out.println("Main Mathod");

	    }
}


