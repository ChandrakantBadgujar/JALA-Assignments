package _02_operators;

public class _3_Equal_notEqual {

	public static void main(String[] args) {
		
		int x = 20;
	    int y = 5;

	    // Equal operator
	    if (x == y) {
	      System.out.println("x is equal to y");
	    } else {
	      System.out.println("x is not equal to y");
	    }

	    // Not equal operator
	    if (x != y) {
	      System.out.println("x is not equal to y");
	    } else {
	      System.out.println("x is equal to y");

	    }	

	}
}
