package _02_operators;

public class _2_Increment_decrement_operators {

	public static void main(String[] args) {
		
		int num = 20;
        System.out.println("Original value of num: " + num);
        
        // Increment operator
        num++;
        System.out.println("After incrementing, num is now: " + num);
        
        // Decrement operator
        num--;
        System.out.println("After decrementing, num is now: " + num);

	}

}
