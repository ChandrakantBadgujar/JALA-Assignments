// Creating an object "person" with properties firstname and lastname
let person = {
    firstname: "Dipak",
    lastname: "Sharma"
  };
  
  // Accessing the properties using dot notation
  console.log(person.firstname); // Output: "Dipak"
  console.log(person.lastname); // Output: "Sharma"
  
  // Accessing the properties using bracket notation
  console.log(person["firstname"]); // Output: "Dipak"
  console.log(person["lastname"]); // Output: "Sharma"